// Create a memoized version of the languages array to avoid recreating it on every render
// Filter out any duplicates by language code
export const languages: Language[] = Array.from(
  new Map(languagesList.map((lang) => [lang.code, lang])).values()
);

// Helper function to get language by code - memoize this for better performance
const languageByCodeCache = new Map<string, Language | undefined>();

export function getLanguageByCode(code: string): Language | undefined {
  if (languageByCodeCache.has(code)) {
    return languageByCodeCache.get(code);
  }

  const language = languages.find((lang) => lang.code === code);
  languageByCodeCache.set(code, language);
  return language;
}
